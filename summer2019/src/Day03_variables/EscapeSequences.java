package Day03_variables;

public class EscapeSequences {
	/* \n : start from new line
	 * \t : start new line with horizontal tab( paragraf )
	 * \\ : print back slash
	 * \' : print single quote
	 * \" : print double quote
	 */
	
	
	
	
	public static void main(String[] args) {
		System.out.println("Hello cybertek\n Batch 12 student\n\n");
		System.out.println("\tClass start tomorrow");
		System.out.println("\\yusuf");
		System.out.println("\'Yusuf");
		System.out.println("The book name is \"game of the year\"");
		
	}

}
