package Day10_IfStatement;

public class without_CurlyBraces {
	
	public static void main(String[] args) {
		
		
	if (false) {   // you can put more than one one in to curly braces.
	  System.out.println("To day is friday");
	  System.out.println("Tomorrow day is off");
	  System.out.println("Enjoy day off");
	}
	 
	if (true)            // without curly braces we can write only one line.
		System.out.println("hello");
	
	if (true)
		System.out.println("if block");
	else if (true)
		System.out.println("else-if block");
	else
		System.out.println("elseblock");
	
	
	
	
	
	
	
	
	}

}
